let firstNum = 10;
let secondNum = 2;

console.log(`Sum: ${firstNum + secondNum}`);
console.log(`Difference: ${firstNum - secondNum}`);
console.log(`Product: ${firstNum * secondNum}`);
console.log(`Quotient: ${firstNum / secondNum}`);
console.log(`Remainder: ${firstNum % secondNum}`);
console.log(`Value of num1 after increment: ${++firstNum}`);
console.log(`Value of num2 after increment: ${--secondNum}`);